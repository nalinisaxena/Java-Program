class stringdemo
{
 public static void main(String args[])
 {
  String color=" ";
  
  switch(color)
  {
   case "red":
              System.out.println("color is red");
              break;
  case "blue":
              System.out.println("color is blue");
              break;
              default:
              System.out.println("color not found");
    }
 }
}    
